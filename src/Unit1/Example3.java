package Unit1;

public class Example3 {

    public static void main(String[] args) {
        int var;
        double x;

        var = 10;
        x = 10.0;

        System.out.println("var = " + var);
        System.out.println("x = " + x);
        System.out.println();

        var = var / 4;
        x = x / 4;

        System.out.println("var / 4 = " + var);
        System.out.println("x / 4 = " + x);
    }

}
